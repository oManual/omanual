<p>
The <code>.omanual</code> file format is an XML-based file
package which can be used to easily package, distribute, view and
import your data and referenced media files.
These files are compressed and packaged using the <a href="https://www.pkware.com/documents/casestudies/APPNOTE.TXT">.ZIP file
format</a>, which is an industry standard for cross-platform data exchange and
storage.
</p>

<p>
File examples and schema definition (XSD)
files are available below to validate your own oManual XML documents.
</p>

